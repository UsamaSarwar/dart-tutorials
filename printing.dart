// Printing in Dart
main() {
  print('Hello World'); // String
  print("'You Name'");
  print('"You Name"');
  print("\"You Name\"");
  print('I\'m Usama Sarwar.');
  print('\$100');
  print(25); // Integer
  print(25.5); // Double
  print(true); // Boolean
  print(["Usama", "Sarwar", 27, true]); // List
  print({"firstName": "Usama", "lastName": "Sarwar"}); // Map
  print({"Pakistan", "India", "China"}); // Set
}

