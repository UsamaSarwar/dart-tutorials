void main() {
  // Hello World!
  String message = "Hello World!";
  print(message);

  // My name is Usama Sarwar.
  String name = "Usama Sarwar";
  print('My name is $name.');

  // I'm 20 years old.
  int age = 20;
  print('I\'m $age years old.');

  // I live in "Lahore".
  String city = "Lahore";
  print('I live in "$city".');

  // I'm a student at "UET".
  String university = "UET";
  print('I\'m a student at "$university".');

  // List: [Pizza, Blue, Cat]
  List<String> list = ["Pizza", "Blue", "Cat"];
  print('List: $list');

  // Set: {Usama, Ali, Muhammad}
  Set<String> set = {"Usama", "Ali", "Muhammad"};
  print('Set: $set');

  // Map: {name: Usama Sarwar, age: 20, city: Lahore, school: UET, favorite food: Pizza, favorite color: Blue, favorite animal: Cat}
  Map<String, String> map = {"name": "Usama Sarwar", "age": "20", "city": "Lahore", "school": "UET", "favorite food": "Pizza", "favorite color": "Blue", "favorite animal": "Cat"};
  print('Map: $map');
}
